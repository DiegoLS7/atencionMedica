    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            String codigo = txtCodigo.getText();
            String paciente = txtPaciente.getText();
            String doctor = txtDoctor.getText();
            int precio = Integer.parseInt(spnPrecio.getValue().toString());
            char categoria;
            if(rbtNormal.isSelected()){
                categoria = 'N';
            }else {
                categoria = 'U';
            }
            
            if(codigo.trim().length()<2){
                JOptionPane.showMessageDialog(this, "El codigo debe tener al menos 2 caracteres");
                txtCodigo.requestFocus();
            }else if(paciente.trim().length()<5){
                JOptionPane.showMessageDialog(this, "El nombre del paciente debe tener almenos 5 carqacteres");
                txtPaciente.requestFocus();
            }else if(doctor.trim().length()<5){
                JOptionPane.showMessageDialog(this, "EL nombre del Doctor debe tener al menos 5 caracteres");
                txtDoctor.requestFocus();
            }else if (precio<1){
                JOptionPane.showMessageDialog(this, "El precio debe ser mayor a 1 ");
                spnPrecio.requestFocus();
            }else {
                Atencion ate = new Atencion(codigo, paciente, doctor, precio, categoria);
                if(lista.agregarAtencion(ate)){
                JOptionPane.showMessageDialog(this, "EMPLEADO REGISTRADO");
                listar();
                }else {
                JOptionPane.showMessageDialog(this, "El Codigo se encuentra registrado");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error aproximado"+e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

